/**
 * Script para corregir las conexiones del router
 * 1. router (route-2) debe ir a whatsapp-solicitar-datos (no a woocommerce)
 * 2. whatsapp-solicitar-datos ya est√° conectado a woocommerce ‚úÖ
 */

const { MongoClient, ObjectId } = require('mongodb');

const MONGO_URI = 'mongodb+srv://momento_admin:admin@clustermomento.zafwwji.mongodb.net/neural_chatbot';
const FLOW_ID = '695a156681f6d67f0ae9cf40';

async function corregirConexionesRouter() {
  const client = new MongoClient(MONGO_URI);
  
  try {
    await client.connect();
    console.log('‚úÖ Conectado a MongoDB\n');
    
    const db = client.db('neural_chatbot');
    
    const flow = await db.collection('flows').findOne({ 
      _id: new ObjectId(FLOW_ID) 
    });
    
    if (!flow) {
      throw new Error('‚ùå Flujo no encontrado');
    }
    
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('üîß CORRIGIENDO CONEXIONES DEL ROUTER');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');
    
    // Agregar conexi√≥n: router (route-2) ‚Üí whatsapp-solicitar-datos
    const nuevaConexion = {
      id: 'edge-router-solicitar',
      source: 'router',
      target: 'whatsapp-solicitar-datos',
      sourceHandle: 'route-2',
      type: 'default',
      animated: false
    };
    
    // Verificar si ya existe
    const existeConexion = flow.edges.find(e => e.id === nuevaConexion.id);
    
    if (!existeConexion) {
      console.log('‚úÖ Agregando: router (route-2) ‚Üí whatsapp-solicitar-datos');
      flow.edges.push(nuevaConexion);
    } else {
      console.log('‚ÑπÔ∏è  Conexi√≥n ya existe');
    }
    
    // Guardar
    await db.collection('flows').updateOne(
      { _id: new ObjectId(FLOW_ID) },
      { 
        $set: { 
          edges: flow.edges,
          updatedAt: new Date()
        } 
      }
    );
    
    console.log('\n‚úÖ Conexiones corregidas');
    console.log(`   Total edges: ${flow.edges.length}`);
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');
    
  } catch (error) {
    console.error('‚ùå Error:', error);
    throw error;
  } finally {
    await client.close();
  }
}

// Ejecutar
corregirConexionesRouter()
  .then(() => {
    console.log('‚úÖ Script completado\n');
    process.exit(0);
  })
  .catch((error) => {
    console.error('\n‚ùå Script fall√≥:', error);
    process.exit(1);
  });
